public class Atividade2 {
    public static void main(String[] args) {
        // Declaração e inicialização do vetor
        int[] numeros = {3, 6, 9, 12};

        // Calcula a soma do primeiro e último elementos
        int soma = numeros[0] + numeros[numeros.length - 1];

        // Exibe o resultado
        System.out.println("A soma do primeiro e último elementos é: " + soma);
    }
}

